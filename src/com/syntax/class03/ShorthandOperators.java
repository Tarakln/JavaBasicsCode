package com.syntax.class03;

public class ShorthandOperators {

	public static void main(String[] args) {
		int num=100;
		num=num+100;
		
		//shorthand version
		num+=100; //same as num=num+100
		
		num-=10; //same as num=num-10
		
		num/=10;
		
		num*=2;
		
		num%=2;
		
		System.out.println(num);
		
	
	
	}

}
