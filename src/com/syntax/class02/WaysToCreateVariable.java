package com.syntax.class02;

public class WaysToCreateVariable {

	public static void main(String[] args) {
		
		//to create a variable we need 2 things: Data type & Name
		
		/* 1st way to create a variable (when we know the value)
		 * Datatype Name = Value */
		int temperature=60;
		
		/*2nd way to create a variable
		 * done in 2 steps
		 * 1. Create a variable (datatype and name)
		 * 2. Assign the value*/
		int sum; //create/declare a variable
		sum=30; //assign/initialize the value
		sum=40; //reassign the value
		
		int num1, num2, num3; //all in one line
		num1=10; //assign the value
		num2=20; //assign the value
		num3=30; //assign the value

		
		

	}

}
